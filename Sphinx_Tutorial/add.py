def add_numbers(a, b):
    """
    Add two numbers and return the result.

    Parameters:
        a (int or float): The first number.
        b (int or float): The second number.

    Returns:
        int or float: The sum of `a` and `b`.

    Example:
        >>> add_numbers(2, 3)
        5
    """
    return a + b


