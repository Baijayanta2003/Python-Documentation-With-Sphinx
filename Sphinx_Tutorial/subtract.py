def subtract_numbers(a,b):
    """
    Subtract two numbers and return the result.

    Parameters:
        a (int or float): The first number.
        b (int or float): The second number.

    Returns:
        int or float: The subtract of `a` and `b`.

    Example:
        >>> subtract_numbers(2, 3)
        -1
    """
    return a-b
